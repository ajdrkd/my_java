package chap04;

public class Sample05 {

	/**
	 * 비교연산자
	 * @param args
	 * @since 1.0
	 * 2021. 10. 27.
	 */
	public static void main(String[] args) {
		int x = 1;
		float y = 2.0f;
		System.out.println(x < y);  //true
		System.out.println(x > y ); //false
		System.out.println(x <= y); //true
		System.out.println(x >= y); //false
		System.out.println(x == y); //false
		System.out.println(x != y); //true
	}

}
